package practice;

public class LearnDoWhileLoop {

    public void doWhileLoop(){
        // print numbers from 1 to 100
        int x = 1;
        do{
            System.out.println("Value of x is: " + x);
            x++;
        }while (x > 89);
    }
    public void doWhileLoop2(){
        //print numbers from 11 to 20
        int y = 11;
        do{
            System.out.println("Numbers are: " + y);
            y++;
        }while (y <= 20);
    }
}