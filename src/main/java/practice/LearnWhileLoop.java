package practice;

public class LearnWhileLoop {
    public void whileLoop() {
        // Print numbers from 11 to 20
         int x = 11;
         while(x <= 20){
             System.out.println("sum result of x:" + x );
             x++;
         }
    }
    public void whileLoop2(){
        // print number from 1 to 100
        int y = 1;
        while(y < 101){
            System.out.println(y);
            y++;
        }
    }


}
