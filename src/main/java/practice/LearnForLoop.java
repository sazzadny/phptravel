package practice;

public class LearnForLoop {
    // print numbers from 1 to 100
    public void forLoop(){
        for (int x=1;x<101;x++){
            System.out.println(" Number are: " + x );
        }
    }
    // print numbers from 11 t0 20

    public void forLoop2(){
        for (int y = 11; y<=20; y++){
            System.out.println("Numbers are: " + y );
        }
    }
}
